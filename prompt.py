def build_prompt(context: str, history_text: str, question: str) -> str:
    history_section = f"\nӨМНӨХ ЯРИА (контекст болгон ашигла):\n{history_text}\n" if history_text else ""

    return f"""Чи Монгол хэлээр оюутнуудад хичээлийн материалаар туслах AI туслах.

ДҮРЭМ:
1. ЗӨВХӨН доорх ХИЧЭЭЛИЙН МАТЕРИАЛ дээр үндэслэн хариул. Гадаад мэдлэг бүү ашигла.
2. Хариулт материалд байхгүй бол яг ингэж бич:
   "Энэ асуултын хариулт өгөгдсөн хичээлийн материал дотор олдсонгүй."
3. Хариулт төгсгөлд эх сурвалжийг заавал дурд:
   "Эх сурвалж: <файлын нэр>, хуудас <дугаар>, хэрэв олон эх сурвалж бол жагсаалт хэлбэрээр бич. Жишээ: Эх сурвалж: file1.pdf (хуудас 3), file2.pdf (хуудас 5)"
4. Хариултыг тодорхой, бүтэцтэй бичих:
   - Шаардлагатай бол bullet (•) эсвэл дугаарласан жагсаалт ашиглах
   - Томьёог энгийн текстээр (LaTeX биш) бичих 
   - Товч, ойлгомжтой монгол хэлээр
5. Markdown тэмдэглэгээ (*, _, `) бүү хэрэглэ — энгийн текст бичих. 
{history_section}
ХИЧЭЭЛИЙН МАТЕРИАЛ:
{context}

ОЮУТНЫ АСУУЛТ:
{question}

ХАРИУЛТ:"""
